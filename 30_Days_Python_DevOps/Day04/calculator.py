num1 = 10
num2 = 5

def add(num1, num2):
    return num1 + num2

def sub(num1, num2):
    return num1 - num2

def mul(num1, num2):
    return num1 * num2

def div(num1, num2):
    return num1 / num2

print("Addition: " + str(add(num1, num2)))
print("Substraction: "+ str(sub(num1, num2)))
print("Multiplication: "+ str(mul(num1, num2)))
print("Division: "+ str(div(num1, num2)))